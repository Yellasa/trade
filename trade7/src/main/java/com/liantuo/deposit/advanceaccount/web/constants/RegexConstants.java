package com.liantuo.deposit.advanceaccount.web.constants;

public class RegexConstants {
	/**
	 * partnerid 验证规则 ：17 数字
	 */
	public static final String PARTNERID_REGEX="\\d{17}";
	/**
	 *  poolNo 验证规则 ：19_32位 数字和字符的组合
	 */
	public static final String POOLNO_REGEX="[\\d|a-zA-Z]{19,32}";
	/**
	 * accountNo 验证规则 ：10_32位 数字和字符的组合
	 */
	public static final String ACCOUNTNO_REGEX="[\\d|a-zA-Z]{10,32}";

	/**
	 * currentPage 验证规则 ：默认0页，0表示第一页 如总页数为20页，则分页页数为0-19页
	 */
	public static final String CURRENTPAGE_REGEX="^[1-9]\\d{0,8}|0";
	/**
	 * showCount 验证规则 ：小于等于100的正整数
	 */
	public static final String SHOWCOUNT_REGEX="^[1-9]\\d{0,1}|100";
	/**
	 * 资金池查询接口showCount 验证规则 ：小于等于1000的正整数
	 */
	public static final String POOL_SHOWCOUNT_REGEX="^[1-9]\\d{0,2}|1000";

	/**
	 * 资金池查询接口coreMerchantNO_REGEX 验证规则 ：小于等于1000的正整数
	 */
	public static final String coreMerchantNO_REGEX="[\\d|a-zA-Z|_]{2,32}";

	 /**
	  * accountType 验证规则 ：1 2 3
	  */
        public static final String ACCOUNTTYPE_REGEX="[1|2|3]";
}
