public class AmountConvertException extends BusinessUncheckedException{

	private static final long serialVersionUID = -6314900511311350969L;

	public AmountConvertException() {
    }

    public AmountConvertException(String message) {
        super(message);
    }

    public AmountConvertException(String message, Throwable cause) {
        super(message, cause);
    }

    public AmountConvertException(Throwable cause) {
        super(cause);
    }
}
