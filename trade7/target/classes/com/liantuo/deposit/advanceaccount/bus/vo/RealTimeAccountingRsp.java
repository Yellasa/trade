package com.liantuo.deposit.advanceaccount.bus.vo;

public class RealTimeAccountingRsp {
	
	/**
	* @字段名 success : S-成功，F-失败
	*/
	private String success;
	
	/**
	* @字段名 retCode : 返回码
	*/
	private String retCode;
	
	/**
	* @字段名 errMessage : 错误描述
	*/
	private String errMessage;
	
	/**
	* @字段名 accountingRecord :相应账户的账务历史
	*/
	private AccountingRecord accountingRecord;


	public String getErrMessage() {
		return errMessage;
	}

	public void setErrMessage(String errMessage) {
		this.errMessage = errMessage;
	}

	public AccountingRecord getAccountingRecord() {
		return accountingRecord;
	}

	public void setAccountingRecord(AccountingRecord accountingRecord) {
		this.accountingRecord = accountingRecord;
	}

	public String getRetCode() {
		return retCode;
	}

	public String getSuccess() {
		return success;
	}

	public void setSuccess(String success) {
		this.success = success;
	}

	public void setRetCode(String retCode) {
		this.retCode = retCode;
	}
}
