package com.liantuo.trade.bus.vo;


/**
 * serialNum        流水号<br/>
 * receiveAccount   收款方账号<br/>
 * receiverName     收款账号姓名<br/>
 * amount           付款金额<br/>
 * resultCode       失败标识(F)/成功标识（S）<br/>
 * errorCode        失败原因/成功原因（null）<br/>
 * alipayTradeNo    支付宝内部流水号<br/>
 * completeTime     完成时间<br/>
 */
public class WithdrawNotifyPayInfoVo extends ANotifyPayInfoVo {

    private String serialNum;

    private String receiveAccount;

    private String receiverName;

    private String amount;

    private String resultCode;

    private String errorCode;

    private String alipayTradeNo;

    private String completeTime;

    public String getSerialNum() {
        return serialNum;
    }

    public void setSerialNum(String tradeNo) {
        this.serialNum = tradeNo;
    }

    public String getReceiveAccount() {
        return receiveAccount;
    }

    public void setReceiveAccount(String receiveAccount) {
        this.receiveAccount = receiveAccount;
    }

    public String getReceiverName() {
        return receiverName;
    }

    public void setReceiverName(String receiverName) {
        this.receiverName = receiverName;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getResultCode() {
        return resultCode;
    }

    public void setResultCode(String resultCode) {
        this.resultCode = resultCode;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getAlipayTradeNo() {
        return alipayTradeNo;
    }

    public void setAlipayTradeNo(String alipayTradeNo) {
        this.alipayTradeNo = alipayTradeNo;
    }

    public String getCompleteTime() {
        return completeTime;
    }

    public void setCompleteTime(String completeTime) {
        this.completeTime = completeTime;
    }

}