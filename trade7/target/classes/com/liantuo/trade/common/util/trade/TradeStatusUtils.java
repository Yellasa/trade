package com.liantuo.trade.common.util.trade;

import java.util.Arrays;
import java.util.Collections;
import java.util.List;

public class TradeStatusUtils {
    public static List<String> parseToList(String content, String spliter) {
        List<String> listStatus = Arrays.asList(content.split(spliter));
        Collections.sort(listStatus);
        return listStatus;
    }

}
